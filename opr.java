class opr
{
    public static void main(String s[])
    {
        System.out.print("ANS 5|7 Is:"+(5|7));
        System.out.print("\nANS 5&7 Is:"+(5&7));
        System.out.print("\nANS 5^7 Is:"+(5^7));
        System.out.print("\nANS ~7 Is:"+(~7));
        System.out.print("\nANS 7>>2 Is:"+(7>>2));
        System.out.print("\nANS 7<<2 Is:"+(7<<2));
    }
}